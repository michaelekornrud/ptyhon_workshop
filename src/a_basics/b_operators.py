# Operators in Python
#
# Arithmetic Operators
a = 10
b = 3
sum_result = a + b  # Addition
diff_result = a - b  # Subtraction
prod_result = a * b  # Multiplication
div_result = a / b  # Division (float result)
int_div_result = a // b  # Integer Division
mod_result = a % b  # Modulus (remainder)
exp_result = a ** b  # Exponentiation

# Comparison Operators
is_equal = a == b  # Equal to
is_not_equal = a != b  # Not equal to
is_greater = a > b  # Greater than
is_less = a < b  # Less than
is_greater_equal = a >= b  # Greater than or equal to
is_less_equal = a <= b  # Less than or equal to

# Logical Operators
x = True
y = False
and_result = x and y  # Logical AND
or_result = x or y  # Logical OR
not_result = not x  # Logical NOT

# Bitwise Operators
bit_a = 5  # 0b0101
bit_b = 3  # 0b0011
bit_and = bit_a & bit_b  # Bitwise AND
bit_or = bit_a | bit_b  # Bitwise OR
bit_xor = bit_a ^ bit_b  # Bitwise XOR
bit_not = ~bit_a  # Bitwise NOT
bit_left_shift = bit_a << 1  # Left shift
bit_right_shift = bit_a >> 1  # Right shift

# Assignment Operators
x = 5  # Assignment
x += 2  # Increment by 2 (equivalent to x = x + 2)
x -= 1  # Decrement by 1 (equivalent to x = x - 1)
x *= 3  # Multiply by 3 (equivalent to x = x * 3)
x /= 2  # Divide by 2 (equivalent to x = x / 2)
x //= 2  # Integer divide by 2 (equivalent to x = x // 2)
x %= 3  # Modulus by 3 (equivalent to x = x % 3)
x **= 2  # Exponentiate by 2 (equivalent to x = x ** 2)
x &= 1  # Bitwise AND with 1 (equivalent to x = x & 1)
x |= 2  # Bitwise OR with 2 (equivalent to x = x | 2)
x ^= 3  # Bitwise XOR with 3 (equivalent to x = x ^ 3)
x <<= 1  # Left shift by 1 (equivalent to x = x << 1)
x >>= 1  # Right shift by 1 (equivalent to x = x >> 1)  