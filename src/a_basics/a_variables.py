# Variables in Python

first_name = "Michael"
last_name = "Ekornrud"

full_name = f"{first_name} {last_name}" # Using f-string for formatting

age = 30  # Integer variable
height = 1.85  # Float variable (double in some languages)
is_student = False  # Boolean variable


# Variable Naming Conventions
# Use snake_case for variable names
user_age = 25
user_height = 1.75

# Constants (should be in uppercase)
PI = 3.14159  # Constant value

# Dynamic Typing
# Python allows changing the type of a variable
dynamic_var = "Hello"  # Initially a string
dynamic_var = 42  # Now an integer

# Type Annotations is a way to indicate the expected type of a variable
filename: str = "data.txt"  # Annotating variable type
count: int = 10  # Annotating variable type
height: float = 1.75  # Annotating variable type


